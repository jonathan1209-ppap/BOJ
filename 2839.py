dp=[0]*10000
dp[0]=0

a=int(input())

for i in range(a+1):
    dp[i]=5001

dp[3]=1
dp[5]=1

for i in range(6,a+1):
    dp[i]=min(dp[i-3],dp[i-5])+1

if dp[a]>=5001:print(-1)
else:print(dp[a])
